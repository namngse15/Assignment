package model;

/**
 *
 * @author tenhik
 */
public class Price {
    private int id;
    private String amount;

    public Price() {
    }

    public Price(int id, String amount) {
        this.id = id;
        this.amount = amount;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }
    
}
